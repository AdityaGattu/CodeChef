#include<iostream>
using namespace std;
int main()
{
    int t;
    cin>>t;
    while(t--)
    {
        long long n;
        string s;
        
        cin>>n>>s;
        long long count=0,ans=0;
        for(int i=0;i<n;i++)
        {
            if(s[i]=='1')
            {count++;
            ans+=count;
            }
        }
        cout<<ans<<"\n";
    }
}